# =============== FOR ===============
for i in range(3):
    print(i, "Hello")

# ---- от и до ----
for i in range(2, 5):
    print(i, "Hello")

# ---- от и до с шагом ----
for i in range(1, 7, 2):
    print(i, "Hello")

# ============== WHILE ==============
count = 0
while count < 5:
    print(count, "Hello")
    count += 1
